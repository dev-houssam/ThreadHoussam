uint64_t generateInstruction(){
    //0x00FFFFFF00000000
    //srand(time(NULL));
    uint64_t _i = ((uint64_t) (rand() % 1200)) | 0x0000000000000000;
    return _i;
}


void * hand(void * g){
    while(1)
        srand(time(NULL));
    return NULL;
}

uint64_t registers[2];

int main()
{
    printf("Hello Mirtille Tester ! How are you today ?");
    printf("Count: %ld\n", ME_OPCODE_COUNT);
    int cpt = 0;
    
    pthread_t mt;
    
    pthread_create(&mt, NULL, hand, NULL);
    pthread_detach(mt);
    
    registers[0] = 0;
    registers[1] = 1;
    
    while(cpt < 10000){
        uint64_t instruction = generateInstruction();
        switch(instruction){
            case ME_ADD:
                printf("add::PC= %ld | instruction=%ld\n", cpt, instruction);
                printf("Before: REG1= %ld | REG2=%ld\n", registers[0], registers[1]);
                registers[0] = registers[0] + registers[1];
                printf("After: REG1= %ld | REG2=%ld\n", registers[0], registers[1]);
                printf("ADD\n");
                break;
            case ME_SUB:
                printf("sub::PC= %ld | instruction=%ld\n", cpt, instruction);
                printf("Before: REG1= %ld | REG2=%ld\n", registers[0], registers[1]);
                registers[0] = registers[0] - registers[1];
                printf("After: REG1= %ld | REG2=%ld\n", registers[0], registers[1]);
                printf("SUB\n");
                break;
            default:
                //printf("Broken\n");
                break;
        }
        cpt++;
    }
    
    return 0;
}
